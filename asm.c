#include <stdio.h>

char main(void)
{
	FILE* file = fopen("pon", "wb");
	
	const unsigned char buffer[100] =
		{ 0x0, 0x1, 0x1, 0x0, 0x0,  // mov eax, $0
		  0x0, 0x3, 0x1, 0x0, 0x48, // mov ecx, $48 ;'H'
		  0x2, 0x1, 0x0, 0x1, 0x3,  // store .eax, ecx
		  0x3, 0x1, 0x0, 0x1, 0x0,  // add $1
		  0x0, 0x3, 0x1, 0x0, 0x69, // mov ecx, $69 ;'i'
		  0x2, 0x1, 0x0, 0x1, 0x3,  // store .eax, ecx
		  0x4						// end 
		};

	fwrite(buffer, 1, 100, file);
	fclose(file);

	return 0;
}
